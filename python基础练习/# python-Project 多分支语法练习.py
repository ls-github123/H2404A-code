# 百分制成绩转换为等级制成绩


score = float (input ('请输入当前卷面成绩：'))

if score >= 90:
    print ('成绩等级为 < A >')

elif score >= 80:
    print ('成绩等级为 < B >')

elif score >= 70:
    print ('成绩等级为 < C >')

elif score >= 60:
    print ('成绩等级为 < D >')

else:
    print ('成绩等级为 < none >')