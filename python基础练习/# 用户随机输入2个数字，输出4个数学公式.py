# 用户随机输入2个数字
# 例：num1 = 10  num2 = 20 
# 要求输出为4个数学公式：10 + 20 = 30、10 - 20 = -10、10 * 20 = 200、10 / 20 = 0.5

num_1 = int(input("请输入数字1："))

num_2 = int(input("请输入数字2："))

print (f"{num_1} + {num_2} = {num_1+num_2}  {num_1} - {num_2} = {num_1-num_2}  {num_1} * {num_2} = {num_1*num_2} {num_1} / {num_2} = {num_1/num_2}")